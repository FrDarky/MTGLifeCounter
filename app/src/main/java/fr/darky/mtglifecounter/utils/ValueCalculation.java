package fr.darky.mtglifecounter.utils;

public class ValueCalculation {
    static public int addShort(int value) {
        return value+1;
    }

    static public int addLong(int value) {
        return value+5;
    }

    static public int removeShort(int value) {
        return value-1;
    }

    static public int removeLong(int value) {
        return value-5;
    }
}
